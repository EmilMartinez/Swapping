package Swapping;

public class Swap {
    public static void main(String[] args) {
        int num1 = 10;
        int num2 = 20;
        int temp;

        System.out.println(num1);
        System.out.println(num2);

        temp = num1; // the method.
        num1 = num2;
        num2 = temp;

        System.out.println("after swapping");
        System.out.println(num1);
        System.out.println(num2);
    }












}
